export function applyTheme(themeId) {
  const theme = THEMES[themeId]
  if (!theme) return

  const root = document.documentElement.style
  root.setProperty('--crt-green', theme.primary)
  root.setProperty('--crt-green-dim', theme.primaryDim)
  root.setProperty('--crt-green-dark', theme.primaryDark)
  root.setProperty('--crt-green-glow', theme.primaryGlow)
  root.setProperty('--crt-amber', theme.accent)
  root.setProperty('--crt-amber-dim', theme.accentDim)
  root.setProperty('--crt-amber-dark', theme.accentDark)
  root.setProperty('--crt-red', theme.danger)
  root.setProperty('--crt-cyan', theme.info)
  root.setProperty('--crt-bg', theme.bg)
  root.setProperty('--crt-bg-light', theme.bgLight)
}

export function getCanvasColors(themeId) {
  const theme = THEMES[themeId]
  if (!theme) return null
  return {
    bg: theme.bg,
    hidden: theme.hidden,
    hiddenBorder: theme.hiddenBorder,
    hiddenHover: theme.hiddenHover,
    hiddenDot: theme.hiddenDot,
    revealed: theme.revealed,
    revealedBorder: theme.revealedBorder,
    mine: theme.danger,
    flag: theme.accent,
    flagStroke: theme.accentDim,
    text: theme.numberColors,
    scanOverlay: theme.scanOverlay,
    scanMine: `${theme.danger}99`,
    scanSafe: theme.scanSafe,
    explosion: theme.danger,
    packetSnifferText: theme.packetSnifferText,
  }
}
